export function checkRolePermission(roleName: string, permissionKey: string): boolean {
  const allowedPermissions = ROLE_PERMISSIONS[roleName as StaffRole];
  if (!allowedPermissions) return false;
  return allowedPermissions.includes(permissionKey as PermissionKey);
}
